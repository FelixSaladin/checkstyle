public class SimplifyBooleanExpression {
    /**
     * 
     */
    public SimplifyBooleanExpression() {
        boolean a = true;
        boolean b = false;
        a = false;
        b = true;
        if (a && b) {
            System.out.println();
        }
        if (a == true) {
            System.out.println();
        }
    }
}
