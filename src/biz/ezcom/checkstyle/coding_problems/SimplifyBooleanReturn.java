public class SimplifyBooleanReturn {
    /**
     * 
     */
    public SimplifyBooleanReturn() {
    }

    /**
     * @return true
     */
    public final boolean isExist() {
        final boolean b = true;
        if (b) {
            return true;
        } else {
            return false;
        }
    }
}
